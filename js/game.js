///////// Camara para enfocar al personaje
function Camera() { 
	this.x = 0; 
	this.y = 0; 
} 
Camera.prototype = { 
	constructor: Camera, 
	focus: function (x, y) { 
		this.x = x - canvas.width / 2; 
		this.y = y - canvas.height / 2; 
		if (this.x < 0) { 
			this.x = 0; 
		} else if (this.x > worldWidth - canvas.width) { 
			this.x = worldWidth - canvas.width; 
		} 
		if (this.y < 0) { 
			this.y = 0; 
		} else if (this.y > worldHeight - canvas.height) { 
			this.y = worldHeight - canvas.height; 
		} 
	} 
}


////////////// DIBUJO DE LA PANTALLA
function repaint() { 
	window.requestAnimationFrame(repaint); 
	paint(ctx); 
} 

function paint(ctx) { 
	var i = 0, l = 0; 
	ctx.fillStyle = '#5C94FC'; 
	ctx.fillRect(0, 0, canvas.width, canvas.height); 
	ctx.fillStyle = '#fff'; 
	// ctx.fillText('Last Press: ' + player.y, 0, 20); 
	for (var i = wall.length - 1; i >= 0; i--) {
		if( wall[i].type === 1 ){
			wall[i].drawImage(ctx, cam, sprite, 373,124,14,15);
		}else if (wall[i].type === 2){
			wall[i].drawImage(ctx, cam, sprite, 373,103,14,15);
		}else if (wall[i].type === 3){
			wall[i].drawImage(ctx, cam, sprite, 46,198,48,24);
		}else if (wall[i].type === 4){
			wall[i].drawImage(ctx, cam, sprite, 96,198,64,24);
		}else if (wall[i].type === 5){
			wall[i].drawImage(ctx, cam, sprite, 49,176,48,19);
		}else if (wall[i].type === 6){
			wall[i].drawImage(ctx, cam, sprite, 98,160,80,35);
		}
	}
	player.drawImage(ctx, cam, sprite, 23,507,12,15 ); 

	if (pause) { 
		ctx.textAlign = 'center'; 
		if (gameover) { 
			ctx.fillText('GAMEOVER', canvas.width / 2, canvas.height / 2); 
		} else { 
			ctx.fillText('PAUSE', canvas.width / 2, canvas.height / 2); 
		} 
		ctx.textAlign = 'left'; 
	} 
} 

function setMapBlocks(mapBlocks, width, height) { 
	var col = 0, 
		row = 0, 
		rect = null; 
		wall.length = 0; 
	for (row = 0; row < mapBlocks.length; row += 1) {
		for (col = 0; col <  mapBlocks[row].length; col += 1) { 
			if (mapBlocks[row][col] === 1) { 
				rect = new Rectangle2D(col * width, row * height, width, height, true); 
				rect.type = 1;
				wall.push(rect); 
			} else if (mapBlocks[row][col] === 2) { 
				rect = new Rectangle2D(col * width, row * height, width, height, true); 
				rect.type = 2;
				wall.push(rect); 
			} else if (mapBlocks[row][col] === 3) { 
				rect = new Rectangle2D(col * width, row * height, 48, 24, true); 
				rect.type = 3;
				wall.push(rect); 
			} else if (mapBlocks[row][col] === 4) { 
				rect = new Rectangle2D(col * width, row * height, 64, 24, true); 
				rect.type = 4;
				wall.push(rect); 
			} else if (mapBlocks[row][col] === 5) { 
				rect = new Rectangle2D(col * width, (row * height)-4, 48, 19, true); 
				rect.type = 5;
				wall.push(rect); 
			} else if (mapBlocks[row][col] === 6) { 
				rect = new Rectangle2D(col * width, (row * height)-20, 80, 35, true); 
				rect.type = 6;
				wall.push(rect); 
			} 
		} 
	} 
	worldWidth = mapBlocks[0].length * width; 
	worldHeight =  mapBlocks.length * height; 
} 

